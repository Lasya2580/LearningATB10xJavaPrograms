package LearningATB10xJavaPrograms.ex_01_JavaBasics;

public class Lab010_LogicalOperator {
    public static void main(String[] args) {
        int i=10,j=20;
        boolean b = i < j;
        System.out.println(i>10 && j<10);
        System.out.println(i>10 || j>10);
        System.out.println(!b);
    }
}
